export const LS_KEY = "nursery_submissions";
export const LS_PROFILE_KEY = "dae_officer_profile";

export type SeedlingEntry = { name: string; age: string; count: string; graftingCount: string };

export interface Submission {
  id: string;
  region: string;
  district: string;
  upazila: string;
  nurseryName: string;
  mobile: string;
  caretakerName?: string;
  caretakerMobile?: string;
  address?: string;
  geoLocation?: string;
  plantingDate?: string;
  remarks?: string;
  submittedAt?: string;
  synced?: boolean;
  fruitSeedlings?: SeedlingEntry[];
  forestSeedlings?: SeedlingEntry[];
  medicinalSeedlings?: SeedlingEntry[];
}

export function loadSubmissions(): Submission[] {
  try {
    const raw = localStorage.getItem(LS_KEY);
    const parsed = raw ? JSON.parse(raw) : [];
    return Array.isArray(parsed) ? parsed : [];
  } catch { return []; }
}

export function saveSubmissions(subs: Submission[]): void {
  localStorage.setItem(LS_KEY, JSON.stringify(subs));
}

export function countSeedlings(s: Submission) {
  const count = (list?: SeedlingEntry[]) => (list || []).reduce((a, e) => a + (parseInt(e.count) || 0) + (parseInt(e.graftingCount) || 0), 0);
  const fruit = count(s.fruitSeedlings);
  const forest = count(s.forestSeedlings);
  const medicinal = count(s.medicinalSeedlings);
  return { fruit, forest, medicinal, total: fruit + forest + medicinal };
}

export function toBn(n: number): string {
  const d = ['০','১','২','৩','৪','৫','৬','৭','৮','৯'];
  return n.toString().replace(/\d/g, c => d[+c]);
}
