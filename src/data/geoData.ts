// ---------- Lookup helpers ----------

export type GeoMode = 'dae_officer' | 'citizen';

/** Region/Division options for the top-level dropdown, branched by mode. */
export function getRegionOptions(mode: GeoMode): string[] {
  return Object.keys(mode === 'dae_officer' ? DAE_REGION_DISTRICTS : DIVISION_DISTRICTS);
}

export function getDistrictsByRegion(mode: GeoMode, region: string): string[] {
  const table = mode === 'dae_officer' ? DAE_REGION_DISTRICTS : DIVISION_DISTRICTS;
  return table[region] ?? [];
}

export function getUpazilasByDistrict(district: string): string[] {
  return DISTRICT_UPAZILAS[district] ?? [];
}
